DROP TABLE IF EXISTS Customer 
CREATE TABLE Customer
(
   `id` int (11) NOT NULL AUTO_INCREMENT,
   `name` varchar (30) NOT NULL,
   `address` varchar (50) NOT NULL,
   
    PRIMARY KEY (id)
)
ENGINE= InnoDB AUTO_INCREMENT= 85 DEFAULT CHARSET= utf8;

