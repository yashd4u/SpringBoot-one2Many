CREATE TABLE `Product` (
	`id` INT(11) NOT NULL AUTO_INCREMENT,
	`productName` VARCHAR(100) NOT NULL,
	`quantity` INT(11) NOT NULL DEFAULT,
	`price` INT(11) NOT NULL,
	PRIMARY KEY (`id`)
)
COLLATE='utf8_general_ci'
ENGINE=InnoDB
AUTO_INCREMENT=2
